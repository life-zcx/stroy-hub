export const findClosestCity = (lat, lon) => {
  let closest = GEO_LOCATIONS[0];
  let minDistance = Infinity;

  GEO_LOCATIONS.forEach(loc => {
    const dist = Math.sqrt(Math.pow(lat - loc.lat, 2) + Math.pow(lon - loc.lon, 2));
    if (dist < minDistance) {
      minDistance = dist;
      closest = loc;
    }
  });

  return closest.name;
};
